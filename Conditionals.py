weather = input("What's the weather outside? ")

#Brain, thunderstorm, sunny, humid, cold.

if weather == "sunny":
    print("Drink a lot of water, bring sunscreen, and wear shorts")
elif weather == "rainy":
    print("Bring an umbrella and wear a raincoat")
elif weather == "cloudy":
    print("Wear a light jacket and bring a scarf")
elif weather == "snowy":
    print("Wear warm clothes and boots")
else:
    print("I don't know")